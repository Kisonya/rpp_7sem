#!/bin/bash

if [ $# -ne 3 ]; then
  echo "Использование: $0 <число1> <оператор> <число2>"
  echo "Допустимые операторы: + - * /"
  exit 1
fi

num1=$1
op=$2
num2=$3

# Проверка на целые числа
if ! [[ "$num1" =~ ^-?[0-9]+$ ]] || ! [[ "$num2" =~ ^-?[0-9]+$ ]]; then
  echo "Ошибка: только целые числа поддерживаются."
  exit 1
fi

# Проверка деления на ноль
if [[ "$op" == "/" && "$num2" == "0" ]]; then
  echo "Ошибка: деление на ноль."
  exit 1
fi

# Вычисление
case "$op" in
  +) result=$((num1 + num2)) ;;
  -) result=$((num1 - num2)) ;;
  \*) result=$((num1 * num2)) ;;
  /) result=$((num1 / num2)) ;;
  *)
    echo "Ошибка: неизвестный оператор '$op'. Используйте + - * /"
    exit 1
    ;;
esac

echo "Результат: $result"
